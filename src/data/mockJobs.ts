export function addJob(name: string, address: string, documents: string[]): Job {
  const job: Job = {
    id: String(nextId++),
    name,
    address,
    jobDescription: '',
    jobDetail: '',
    dueDate: dueDateFromOverdue(0),
    price: 0,
    amountPaid: 0,
    daysOverdue: 0,
    status: 'Initial wait',
    emails: [],
    phones: [],
    invoiceDocuments: documents,
    contextInstructions: '',
  };
  mockJobs.push(job);
  return job;
}

export function removeJob(jobId: string): Job | undefined {
  const index = mockJobs.findIndex(job => job.id === jobId);
  if (index === -1) {
    return undefined;
  }

  const [removedJob] = mockJobs.splice(index, 1);
  return removedJob;
}
